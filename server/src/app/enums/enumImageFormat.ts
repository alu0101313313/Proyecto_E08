/**
 * Enum que indica los posibles formatos de la imagen desplegada de las cartas
 * 
 * @PNG indica que la carta se puede desplegar en formato png
 * @JPG indica que la carta se puede desplegar en formato jpg
 * @WEBP indica que la carta se puede desplegar en formato webp
 */
export enum imageFormat {
  PNG = 'png',
  JPG = 'jpg',
  WEBP = 'webp'
}