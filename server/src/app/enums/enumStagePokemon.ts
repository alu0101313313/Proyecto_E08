
/**
 * Enum que indica el estado evolutivo del pokemon en una carta Pokemon
 * 
 * @BASIC indica que se encuentra en la primera evolución
 * @STAGE_1 indica que se encuentra en la segunda evolución
 * @STAGE_2 indica que se encuentra en la tercera evolución
 */
export enum StagePokemon {
  BASIC = 'Basic',
  STAGE_1 = 'Stage 1',
  STAGE_2 = 'Stage 2',
}